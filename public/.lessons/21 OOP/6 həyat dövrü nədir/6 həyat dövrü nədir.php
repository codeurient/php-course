Obyektin həyat dövrü dedikdə onun yaradılmasından məhv edilməsinə qədər olan bütün mərhələlər nəzərdə tutulur. PHP-də obyektin həyat dövrü ümumiyyətlə üç əsas mərhələdən ibarətdir:




1. Obyektin Yaradılması (Instantiation)
        Bu mərhələdə obyekt bir sinifdən yaradılır və constructor metodu avtomatik olaraq çağırılır.
        Constructor, obyektə ilkin parametrlər təyin edir və onun istifadəyə hazır vəziyyətə gəlməsini təmin edir.





2. Obyektin İstifadəsi (Object Usage)
        Bu mərhələdə obyekt istifadə edilir. Obyektin metodları çağırılır, xüsusiyyətləri oxunur və ya yenilənir. Bu mərhələ obyektin əsas funksionallığını yerinə yetirdiyi mərhələdir.
        Xüsusiyyətləri:
        Obyektin dəyişənləri istifadə edilə və dəyişdirilə bilər. Metodlar vasitəsilə obyektin davranışı idarə olunur.





3. Obyektin Məhv Edilməsi (Destruction)
        Bu mərhələ obyektin artıq istifadəyə yararsız olduğu və yaddaşdan silindiyi mərhələdir.
        Destructor metodu obyekt yaddaşdan silinmədən əvvəl avtomatik olaraq çağırılır. Bu, resursların təmizlənməsi üçün istifadə olunur.

