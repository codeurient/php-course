       Overriding və Polymorphism arasındakı əlaqə

        Overriding nədir?
                Bir bala class (child class), ana class (parent class)-ın metodunu yenidən təyin edir (yenidən yazır).
                Bu, yalnız inheritance zamanı mümkündür.
                Overriding, polymorphism-in bir formasıdır.


        Polymorphism nədir?
                Polimorphism müxtəlif formaların eyni interfeysdən istifadə edərək fərqli davranış sərgiləməsidir.
                Bu, inheritance ilə (overriding vasitəsilə) və ya interfaces vasitəsilə həyata keçirilə bilər.
                Məqsəd: Müxtəlif obyektlərin eyni metod çağırışına fərqli cavab verməsini təmin etməkdir.




        Fərqlər:
        Xüsusiyyət	                            Overriding	                                                        Polymorphism

        Nədir?	                                    Metodun child class-da yenidən yazılmasıdır.	                Eyni metod çağırışının fərqli obyektlərdə fərqli davranmasıdır.
        Nə ilə bağlıdır?	                    Sadəcə inheritance ilə bağlıdır.	                                Həm inheritance, həm də interfaces ilə bağlıdır.
        Məcburiyyət	                            Ana class metodunu yenidən yazmaq istəyəndə olur.	                Müxtəlif obyektlərə vahid bir interfeys təqdim edir.
        İstifadə məqsədi	                    Metodun spesifik davranışını dəyişmək.	                        Objektdən asılı olaraq fərqli davranış əldə etmək.



        Overriding və Polymorphism oxşardır, lakin tam eyni deyildir.
        Overriding, yalnız child class-da ana class metodunun dəyişdirilməsi ilə bağlıdır.
        Polymorphism isə daha geniş bir anlayışdır və obyektlərin eyni interfeysdən istifadə edərək fərqli davranış sərgiləməsini əhatə edir.
        Polymorphism həm overriding-i, həm də interfaces vasitəsilə müxtəlif implementasiyaları əhatə edə bilər.


        Yadda saxlayın:
                Overriding, polymorphism-in bir formasıdır.
                Amma bütün polymorphism nümunələri overriding-i əhatə etmir.
